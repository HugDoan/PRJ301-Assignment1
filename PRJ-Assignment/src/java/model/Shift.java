package model;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Shift {
    private int id;
    private String name;
    private Date start;
    private Date end;

    public Shift() {
    }
    
    public Shift(int id, String sname, Date start, Date end) {
        this.id = id;
        this.name = sname;
        this.start = start;
        this.end = end;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSname() {
        return name;
    }

    public void setSname(String sname) {
        this.name = sname;
    }

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
}
